import math

nA = int(input("Inserisci numero A: "))
nB = int(input("Inserisci numero B: "))

print(nA, " + ", nB ," = " , (nA + nB))
print(nB, " - ", nA ," = " , (nB - nA))
print(nA, " x ", nB ," = " , (nA * nB))
print(nA, " / ", nB ," = " , (nA / nB))
print(nA, " % ", nB ," = " , (nA % nB))
print(nA, " log10 = " , (math.log10(nA)))
print(nA, " ** ", nB ," = " , (nA ** nB))
